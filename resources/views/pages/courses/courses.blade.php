                            <h3>Примеры применения МА курса и Интерактивной карты в учебном процессе</h3>
                            <h4>Невозможно себе представить урок географии или истории без карт и атласов. Однако до последнего момента все они были выполнены традиционно на бумажных носителях. Интерактивные же карты – принципиально иной способ предъявления информации.</h4>
                            <p>Их главное достоинство может оценить любой учитель, которому не раз приходилось, готовясь к уроку, развешивать несколько карт на доске. Обычно при этом приходится выбирать только самые показательные, отказываясь от дополнительных ресурсов в силу ограниченности пространства для демонстрации. К примеру, традиционный урок по страноведению в рамках курса географии 7-го класса может включать в себя работу с политической, климатической, физической картами страны (региона), картами размещения хозяйства, плотности населения, природных зон и так далее (рис. 1). Однако разместить все это на доске невозможно, поэтому учитель исключает из урока подчас необходимый наглядный материал. В случае же использования интерактивных карт учитель не ограничен в выборе иллюстративного материала урока: все, что нужно, можно выводить на экран последовательно, совмещая работу с картами и работу с другими иллюстративными ресурсами.</p>
                            <p>Второй положительный момент заключается в возможности увеличения той или иной области для более детализированной демонстрации. Если, работая с настенной полиграфической картой, учитель может представить ученику лишь «общий план», то интерактивная позволяет изучить мельчайшие, но очень важные подробности, надписи, условные обозначения. Это бывает особенно актуально при работе с большим количеством информации.</p>
